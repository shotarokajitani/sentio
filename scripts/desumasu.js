function transformSentence(s) {
  if (!s) return s;
  // skip if sentence ends with closing quote 」 (someone's words)
  if (s.endsWith("」")) return s;
  for (const [re, rep] of RULES) {
    if (re.test(s)) {
      return s.replace(re, rep);
    }
  }
  return s;
}

function transformBlock(text) {
  // split by 。 keeping order; each segment is the text before a 。
  const parts = text.split("。");
  // last part is text after the final 。 (often empty); don't add a trailing 。
  const transformed = parts.map((p, i) => {
    if (i === parts.length - 1) return transformSentence(p); // tail (no trailing 。)
    return transformSentence(p);
  });
  return transformed.join("。");
}

function processFile(filePath) {
  let html = fs.readFileSync(filePath, "utf8");
  // <p>...</p>
  html = html.replace(/<p>([\s\S]*?)<\/p>/g, (m, inner) => {
    return `<p>${transformBlock(inner)}</p>`;
  });
  // <blockquote>...</blockquote>
  html = html.replace(/<blockquote>([\s\S]*?)<\/blockquote>/g, (m, inner) => {
    return `<blockquote>${transformBlock(inner)}</blockquote>`;
  });
  fs.writeFileSync(filePath, html, "utf8");
}

