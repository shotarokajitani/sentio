// ---- merge extras ----
for (const a of articles) {
  const ex = EXTRAS[a.slug];
  if (!ex) continue;
  if (ex.extraPs) {
    ex.extraPs.forEach((p, i) => {
      if (a.sections[i]) a.sections[i].ps.push(p);
    });
  }
  if (ex.extraSection) a.sections.push(ex.extraSection);
  const ex2 = EXTRAS2[a.slug];
  if (ex2 && ex2.extraPs) {
    ex2.extraPs.forEach((p, i) => {
      if (a.sections[i]) a.sections[i].ps.push(p);
    });
  }
}

// ---- write articles ----
let charCounts = [];
for (const a of articles) {
  const html = renderArticle(a);
  fs.writeFileSync(path.join(BLOG_DIR, `${a.slug}.html`), html, "utf8");
  // count body chars
  const bodyChars = a.sections.reduce(
    (sum, s) => sum + s.ps.join("").length + (s.quote ? s.quote.length : 0),
    0,
  );
  charCounts.push({ slug: a.slug, chars: bodyChars });
}

